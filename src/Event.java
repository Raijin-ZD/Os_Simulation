public enum Event {
	InterruptA, InterruptB, ExceptionA, ExceptionB, ExceptionC
}
/*
   InteruptA: User presses on a key on the keyboard.
   InteruptB: Disk controller finishes reading data.
   ExceptionA: Requesting more heap.
   ExceptionB: Attempts integer division by zero.
   ExceptionC: Attempts to access privileged memory.
*/